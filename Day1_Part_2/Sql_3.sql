-- Number Functions 

-- Abs() : Returns absolute value 

select abs(-12) 

-- power(n,m) : returns n power m value 

select POWER(2,3)

-- Sqrt(n) : Returns sqrt value 

select sqrt(49) 

-- Ceiling() : Returns the greatest integer value 

select CEILING(12.0000000001)

-- Floor() : Returns the smallest integer value 

select floor(12.99999)

-- Round() : Used to round the number to particular digits specified 

select round(8786.8876,3);
select round(8786.8876,2);
select round(8786.8876,1);
select round(8786.8876,0);
select round(8786.8876,-1);
select round(8786.8876,-2);
select round(8786.8876,-3);